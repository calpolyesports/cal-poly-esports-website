<h1>About</h1>
<p>Cal Poly Esports is a student-run organization that aims to provide a competitive and social environment for students interested in video games. We host events, tournaments, and social gatherings for students to meet and compete with others who share their interests. Our goal is to create a community where students can come together to play games, make friends, and have fun.</p>
<h2>Our Mission</h2>
<p>Our mission is to provide a space for students to come together and share their passion for video games. We strive to create a welcoming and inclusive community where students can connect with others who share their interests. We believe that video games have the power to bring people together and create lasting friendships. Our goal is to foster a sense of community and belonging among students who love to play games.</p>
<h2>Our Values</h2>
<p>At Cal Poly Esports, we value inclusivity, diversity, and respect. We believe that everyone should have the opportunity to participate in our events and feel welcome in our community. We are committed to creating a safe and supportive environment for all students, regardless of their background or experience level. We believe that everyone has something valuable to contribute and that we are stronger when we come together as a community.</p>
<h2>Get Involved</h2>
<p>If you are interested in joining Cal Poly Esports, we encourage you to attend one of our events or meetings. You can also follow us on social media to stay up to date on our latest news and announcements. We are always looking for new members to join our community and help us grow. Whether you are a casual player or a competitive gamer, we welcome you to come and be a part of Cal Poly Esports.</p>

